DROP TABLESPACE actiweb;
CREATE TABLESPACE actiweb DATAFILE 'actiweb.dat'  SIZE 10M     REUSE     AUTOEXTEND ON NEXT 10M MAXSIZE 200M;
DROP ROLE actiweb_role;
CREATE ROLE actiweb_role; 
GRANT CREATE ANY PROCEDURE, EXECUTE ANY PROCEDURE, DROP ANY PROCEDURE TO actiweb_role WITH ADMIN OPTION;
DROP USER actiweb ;
CREATE USER actiweb IDENTIFIED BY actiweb DEFAULT TABLESPACE actiweb TEMPORARY TABLESPACE TEMP;
GRANT CONNECT, RESOURCE,DROP PUBLIC SYNONYM,CREATE PUBLIC SYNONYM,CREATE VIEW TO actiweb_role;
GRANT CREATE SESSION TO actiweb_role;
GRANT CREATE SESSION TO actiweb;
ALTER USER actiweb quota unlimited on actiweb;